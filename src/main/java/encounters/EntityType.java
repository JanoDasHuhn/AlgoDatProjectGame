package encounters;

/**
 * Das Enum {@code EntityType} beschreibt die möglichen Typen einer Entität.
 * Eine Entität kann entweder GOOD (gut), NEUTRAL oder EVIL (böse) sein.
 */
public enum EntityType {
    /** Gute Entität */
    GOOD,
    /** Neutrale Entität */
    NEUTRAL,
    /** Böse Entität */
    EVIL
}
