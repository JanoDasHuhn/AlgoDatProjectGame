package inventar.items;

/**
 * Das Enum {@code WeaponType} beschreibt die verschiedenen Typen von Waffen. Mögliche Typen sind:
 * CYBER, BRUTE_FORCE und FAST.
 */
public enum WeaponType {
  /** Cyber-Waffe (technologisch, z.B. Laser, Hacking) */
  CYBER,
  /** Waffe mit roher Gewalt (z.B. Vorschlaghammer, Fäuste) */
  BRUTE_FORCE,
  /** Schnelle Waffe (z.B. Dolch, leichte Pistole) */
  FAST
}
