package inventar.items;

/**
 * Das Enum {@code Rarity} beschreibt die Seltenheit eines Items.
 * Mögliche Stufen sind: COMMON, UNCOMMON, RARE, EPIC, LEGENDARY und MYTHIC.
 */
public enum Rarity {
    /** Gewöhnlich */
    COMMON,
    /** Ungewöhnlich */
    UNCOMMON,
    /** Selten */
    RARE,
    /** Episch */
    EPIC,
    /** Legendär */
    LEGENDARY,
    /** Mythisch */
    MYTHIC
}
